#!/bin/bash
funWithParam(){
    echo "第一个参数为 $1 !"
    echo "第二个参数为 $2 !"
    # 注意：$10 会被解释为 $1 后面跟着字符 0
    echo "第十个参数为 $10 ! (错误写法)"
    # 正确写法是 ${10}
    echo "第十个参数为 ${10} ! (正确写法)"
    echo "第十一个参数为 ${11} !"
    echo "参数总数有 $# 个!"
    echo "作为一个字符串输出所有参数 $* !"
}

# 调用函数并传递大量参数
funWithParam 1 2 3 4 5 6 7 8 9 34 73
